public class FindNumberDivisibleBy3 {
    public static void main(String[] args) {
        int num = 6;

        // Method 1: Using If-Else
        if (num % 3 == 0) {
            System.out.println("Number is divisible by 3");
        } else {
            System.out.println("Number is not divisible by 3");
        }
    }
}