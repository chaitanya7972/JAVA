//WAJP to find volume of cube


public class Volume_cube {
    public static void main(String[] args) {
        // 1. Declare and initialize the variable first
        int side = 5; 
        
        // 2. Perform the calculation using the variable
        int volume = side * side * side;
        
        // 3. Print the result
        System.out.println("The volume of the cube is: " + volume);
    }
}